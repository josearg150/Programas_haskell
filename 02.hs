--Funcion de suma 
suma x y = x + y
--Funcion de resta 
resta x y = x - y
--Funcion de media aritmetica de tres numeros 
mediaTres x y z = (x + y + z) / 3
--Funcion potencia de un numero 
potencia x = x * x 
--Funcion de maximo de tres numeros
maxTres x y z = max x (max y z)
--Funcion de minimo de tres numeros
minTres x y z = min x (min y z)
